			<h3>Comment utiliser SmartShare ?</h3>

			<h5>Envoi de documents</h5>
			<p>
			Pour sélectionner les documents que vous souhaitez joindre à votre message, vous devez successivement :
			<ul>
				<li>appuyer sur le bouton "Joindre des documents"</li>
				<li>sélectionner dans l'arborescence les fichiers correspondants</li>
				<li>cliquer sur "Ouvrir" </li>
			</ul>
			A l'issue de cette opération, SmartShare va automatiquement joindre les documents à votre message, et insérer dans le corps du texte les liens correspondants avec un message d'explication pour les destinataires de votre message.
			</p>
			<h5>Création d'une galerie de documents</h5>
			<p>
			SmartShare vous permet également de générer automatiquement une page internet permettant de visualiser d'un seul coup d'oeil plusieurs documents, et le cas échéant de les télécharger, individuellement ou en une seule fois. Ceci est particulièrement pratique lorsque vous souhaitez partager avec vos correspondants des photographies.
			Pour créer une galerie de documents et l'ajouter à votre message, vous devez successivement :
			<ul>
				<li>appuyer sur le bouton "Ajouter une galerie"</li>
				<li>sélectionner dans l'arborescence les fichiers correspondants</li>
				<li>cliquer sur "Ouvrir"</li>
			</ul>
			A l'issue de cette opération, SmartShare va automatiquement joindre les documents à votre message, et insérer dans le corps du texte le lien correspondant avec un message d'explication pour les destinataires de votre message.
			</p>
			<p>
			<h5>Partage de documents avec authentification</h5>
			Vous avez la possibilité de sécuriser l'envoi de documents confidentiels, en vous assurant que seul(s) le/les destinataire(s) designé(s) seront en mesure d'accéder aux documents que vous transmettez, y compris si votre message était ultérieurement transféré à d'autres correspondants.
			Pour ce faire, vous devez successivement :
			<ul>
				<li>renseigner dans votre message les adresses électroniques des seuls destinataires autorisés</li>
			</ul>
			Une fenêtre s'ouvre alors vous invitant à confirmer la liste des utilisateurs autorisés.
			Après confirmation de la liste, vous devez :
			<ul>
				<li>appuyer sur le bouton "Ajouter une galerie sécurisée"</li>
				<li>sélectionner dans l'arborescence les fichiers correspondants</li>
				<li>cliquer sur "Ouvrir"</li>
			</ul>
			A l'issue de cette opération, SmartShare va automatiquement joindre les documents à votre message, et insérer dans le corps du texte le lien correspondant avec un message d'explication pour les destinataires de votre message.
			</p>
			<h5>Accès à des documents imposant une authentification</h5>
			<p>
			Lorsque vous recevez un lien d'accès à une galerie de documents sécurisée, cliquez sur le lien inclus dans le message pour accéder à la page d'authentification, où il vous sera demandé de renseigner votre adresse de messagerie.
			Après validation, vous recevrez un message contenant un lien temporaire, dont la validité n'excède pas quelques heures, vous permettant d'accéder à la galerie de documents.
			</p>

